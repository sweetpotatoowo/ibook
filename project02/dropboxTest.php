    <p>Bonne année 2024！</p>
    <br>
    <p>2024年，小時候看到這數字，覺得是遙遠未來，腦子出現的影像偏向《銀翼殺手》（Blade Runner，1982年出品）的未來光景，有種華麗破敗，夾帶著末世孤寂；尚未搞清楚，時間是怎麼腳底抹油且溜個精光的，電光石火間，人就站在2024年起始點，實際說來沒有想像中的糟。後疫時代從混沌未明進入重新洗牌，這兩年的混亂漩渦逐漸緩慢下來，被攪亂的事物開始落腳新位子，動盪時局製造了翻盤契機，強勢與弱勢可能瞬間互換，未嘗是一件壞事。
        <br>
        《ELLE》1月號獻上「新奢侈主義」及「編輯報告：2024奢華享樂重點」兩大趨勢主題，率先討論整年度的奢華風向吹向哪兒去；「新奢侈主義」專題可說是時尚編輯Wawa與Wendy之間的專場辯論會，她們各自擁護「經典」和「新興」論述。Wawa支持的是：「正統百年工藝、極簡主義萬歲、黑白灰萬年不敗。」Wendy提出的卻是：「反表面精緻、Dirty Fit沒錢風、Color is new black。」誓不兩立的兩派分別提出實例佐證，與讀者一同探索時尚風格裡的奢侈新定義。
        <br>
        「編輯報告：2024奢華享樂重點」專題，則集結Candy、Coco、Stanley 3位編輯的趨勢預測，他們的開場白是這麼說：「不只住安縵，還要把Aman Style帶回家。不只搭遊輪，還要搭上前進南極的夢幻遊輪；不只買手機，還要買最新最狂的設計；不只從亞洲50大酒吧看趨勢，還要喝出精髓！所謂真正的奢華，是把日常過得非日常、突破想像極致。」一整年的初始，就從安排享樂生活開場，看來是個令人開心的起跑點。
    </p>
