    <p>Me, Lalisa
        <br>
        站上世界表演之巔
        <br>
        為了成功一個人能付出多少，從普通人到成為全球頂流明星，LISA用十年的時間，向14歲遠赴韓國追夢的自己證明：當實力配上野心，再加上比別人更多倍的努力，舞台的燈光與觀眾的視線總會為自己照明，散發奪目光芒讓她綻放，贏得全世界的掌聲。
        <br>
        LISA，是天生的表演者。還記得2019年BLACKPINK首登Coachella舞台，不只打破紀錄成為首個登上Coachella的韓國女團，更創造了Twitter上爆炸性熱搜，許多人都在問那位「#Coachella馬尾、#BLACKPINK瀏海女孩」是誰，賣力演出的LISA一夕收獲了大批全球粉絲。事隔4年，BLACKPINK再次站上Coachella音樂節，刷新紀錄成為亞洲藝人中首次擔任主舞台表演的明星，18首歌曲宛如一場專屬演唱會，讓4位粉紅戰士盡情燃燒、釋放能量，於演出的最後BLACKPINK彼此緊緊相擁，向全世界宣告她們做到了。回想起這兩週炙熱的情緒，LISA興奮地表示：「這次的Coachella舞臺規模比之前的更大，確實也更加緊張，當然是心情好的那種緊張感（笑）。BLACKPINK竟然登上Coachella主舞臺，一定要好好表現！心中只抱著絕對不能辜負大家期待的想法，想要再次向BLINK們展示BLACKPINK自豪的樣子，雖然對此有些緊張，但非常地有趣，是想向所有人炫耀的瞬間。而且是成員們第一次於Coachella上展現個人Solo舞臺，對所有人來說都是很有意義的瞬間。在Coachella音樂節有很多看起來不像是BLINK，而只是來享受這個音樂節慶典的人，他們應該更想知道BLACKPINK到底是誰，特別是對BLACKPINK各個成員們會帶來什麼樣的歌曲和表演，又會呈現什麼樣的風格感到好奇，我們也很開心能透過Coachella來展現給大眾。」
    </p>
